#ifndef __ASDX_FONT_H__
#define __ASDX_FONT_H__

//-------------------------------------------------------------------------------------------------
// Includes
//-------------------------------------------------------------------------------------------------
#include <asdxTypedef.h>
#include <asdxMath.h>
#include <asdxRef.h>
#include <asdxSprite.h>


namespace asdx {

///////////////////////////////////////////////////////////////////////////////////////////////////
// Font class
///////////////////////////////////////////////////////////////////////////////////////////////////
class Font
{
    //=============================================================================================
    // list of friend classes and methods.
    //=============================================================================================
    /* NOTHING */

private:
    //=============================================================================================
    // private variables.
    //=============================================================================================
    /* NOTHING */

    //=============================================================================================
    // private methods.
    //=============================================================================================
    /* NOTHING */

protected:
    //=============================================================================================
    // protected variables.
    //=============================================================================================
    static const size_t NUM_SPRITES = 2048;            //!< 最大スプライト数です.
    RefPtr<ID3D11Texture2D>            m_pTexture;     //!< テクスチャです.
    RefPtr<ID3D11ShaderResourceView>   m_pSRV;         //!< シェーダリソースビューです.

    u32                 m_FontWidth;                //!< フォントの横幅です.
    u32                 m_FontHeight;               //!< フォントの縦幅です.
    char                m_FontName[ 32 ];           //!< フォント名です.
    f32                 m_OffsetU;                  //!< U方向のオフセットです.
    f32                 m_OffsetV;                  //!< V方向のオフセットです.
    char                m_Buffer[ NUM_SPRITES ];    //!< 文字列バッファです.
 
    //=============================================================================================
    // protected methods.
    //=============================================================================================
    /* NOTHING */

public:
    //=============================================================================================
    // public variables.
    //=============================================================================================
    /* NOTHING */

    //=============================================================================================
    // public methods.
    //=============================================================================================

    //---------------------------------------------------------------------------------------------
    //! @brief      コンストラクタです.
    //---------------------------------------------------------------------------------------------
    Font();

    //---------------------------------------------------------------------------------------------
    //! @brief      デストラクタです.
    //---------------------------------------------------------------------------------------------
    virtual ~Font();

    //---------------------------------------------------------------------------------------------
    //! @brief      初期化処理です.
    //!
    //! @param [in]     pDevice         デバイスです.
    //! @param [in]     filename        フォントバイナリファイル名です.
    //! @retval true    初期化に成功.
    //! @retval false   初期化に失敗.
    //---------------------------------------------------------------------------------------------
    bool Init( ID3D11Device* pDevice, const char16* filename );

    //---------------------------------------------------------------------------------------------
    //! @brief      初期化処理です.
    //!
    //! @param [in]     pDevice         デバイスです.
    //! @param [in]     pBinary         フォントバイナリです.
    //! @param [in]     binarySize      フォントバイナリのサイズです.
    //! @retval true    初期化に成功.
    //! @retval false   初期化に失敗.
    //---------------------------------------------------------------------------------------------
    bool Init( ID3D11Device* pDevice, const u8* pBinary, const u32 binarySize );

    //---------------------------------------------------------------------------------------------
    //! @brief      終了処理です.
    //---------------------------------------------------------------------------------------------
    void Term();

    //---------------------------------------------------------------------------------------------
    //! @brief      フォントの横幅を設定します.
    //!
    //! @param [in]     width       フォントの横幅.
    //---------------------------------------------------------------------------------------------
    void SetFontWidth( u32 width );

    //---------------------------------------------------------------------------------------------
    //! @brief      フォントの縦幅を設定します.
    //!
    //! @param [in]     height      フォントの縦幅.
    //---------------------------------------------------------------------------------------------
    void SetFontHeight( u32 height );

    //---------------------------------------------------------------------------------------------
    //! @brief      描画を開始します.
    //!
    //! @parma[in]      pDeviceContext      デバイスコンテキストです.
    //! @param[in]      isMultiSample       マルチサンプルかどうか.
    //---------------------------------------------------------------------------------------------
    void Begin( ID3D11DeviceContext* pDeviceContext, asdx::Sprite& sprite );

    //---------------------------------------------------------------------------------------------
    //! @brief      文字列を描画します.
    //!
    //! @param [in]     x       描画開始X座標.
    //! @param [in]     y       描画開始Y座標.
    //! @param [in]     text    描画するテキスト.
    //---------------------------------------------------------------------------------------------
    void DrawString( asdx::Sprite& sprite, const s32 x, const s32 y, const char* text );

    //---------------------------------------------------------------------------------------------
    //! @brief      文字列を描画します.
    //!
    //! @param [in]     x           描画開始X座標.
    //! @param [in]     y           描画開始Y座標.
    //! @param [in]     layerDepth  レイヤーの深さ.
    //! @param [in]     text        描画するテキスト.
    //---------------------------------------------------------------------------------------------
    void DrawString( asdx::Sprite& sprite, const s32 x, const s32 y, const s32 layerDepth, const char* text );

    //---------------------------------------------------------------------------------------------
    //! @brief      文字列を描画します.
    //!
    //! @param [in]     x           描画開始X座標.
    //! @param [in]     y           描画開始Y座標.
    //! @param [in]     format      書式指定子.
    //! @param [in]     ...         可変引数.
    //---------------------------------------------------------------------------------------------
    void DrawStringArg( asdx::Sprite& sprite, const s32 x, const s32 y, const char* format, ... );

    //---------------------------------------------------------------------------------------------
    //! @brief      文字列を描画します.
    //!
    //! @param [in]     x           描画開始X座標.
    //! @param [in]     y           描画開始Y座標.
    //! @param [in]     layerDepth  レイヤーの深さ.
    //! @param [in]     format      書式指定子.
    //! @param [in]     ...         可変引数.
    //---------------------------------------------------------------------------------------------
    void DrawStringArg( asdx::Sprite& sprite, const s32 x, const s32 y, const s32 layerDepth, const char* format, ... );

    //---------------------------------------------------------------------------------------------
    //! @brief      描画終了処理です.
    //!
    //! @param [in]     pDeviceContext      デバイスコンテキスト.
    //---------------------------------------------------------------------------------------------
    void End( ID3D11DeviceContext* pDeviceContext, asdx::Sprite& sprite );

    //---------------------------------------------------------------------------------------------
    //! @brief      フォントの横幅を取得します.
    //!
    //! @return     フォントの横幅を返却します.
    //---------------------------------------------------------------------------------------------
    u32 GetFontWidth () const;

    //---------------------------------------------------------------------------------------------
    //! @brief      フォントの縦幅を取得します.
    //!
    //! @return     フォントの縦幅を返却します.
    //---------------------------------------------------------------------------------------------
    u32 GetFontHeight() const;

    //---------------------------------------------------------------------------------------------
    //! @brief      フォント名を取得します.
    //!
    //! @return     フォント名を返却します.
    //---------------------------------------------------------------------------------------------
    const char* GetFontName() const;
};

} // namespace asdx

#endif//__ASDX_FONT_H__
