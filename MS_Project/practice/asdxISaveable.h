#ifndef __ASDX_ISAVEABLE_H__
#define __ASDX_ISAVEABLE_H__

//-------------------------------------------------------------------------------------------------
// Includes
//-------------------------------------------------------------------------------------------------
#include <asdxTypedef.h>


namespace asdx {

///////////////////////////////////////////////////////////////////////////////////////////////////
// ISaveable interface
///////////////////////////////////////////////////////////////////////////////////////////////////
struct ISaveable
{
    //---------------------------------------------------------------------------------------------
    //! @brief      ファイルに保存します.
    //!
    //! @param[in]      filename        ファイル名です.
    //! @retval true    保存に成功.
    //! @retval false   保存に失敗.
    //---------------------------------------------------------------------------------------------
    virtual bool Save( const char16* filename ) = 0;
};


} // namespace asdx


#endif//__ASDX_ISAVEABLE_H__
